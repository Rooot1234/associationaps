	<p>Les présents statuts et règlementds intérieur constituent les textes fondateurs de cette organisation</p>

	<h3 id="st">Statuts</h3>

	<h2>TITRE I : DISPOSITIONS GENERALES</h2>
	<H3>Article 1 : Constitution</H3>
	<p> Il est constitué entre les adhérents aux présents statuts une association régie par la loi 064-2015/CNT du 20 octobre 2015 portant 
		liberté d'association au Burkina-Faso.  <br>
    </p> <br>

	<h3>Article 2 : Dénomination </h3>
	<p>
	    L’association dénommée « Association pour la Paix au Sahel et en Savane » en abrégée 
       « <strong> APS </strong> » est apolitique, à but non lucratif, non syndicale, non confessionnelle. Elle 
        n’autorise aucune discrimination de genre et d’ethnie.

	</p> <br>

	<h3>Article 3 : Logo </h3>
	<p>
	   Le logo du APS se reconnait par l’illustration suivante : deux mains ouvertes, un pigeon, des 
       dunes de sables et des buissons traduisant respectivement la concertation et la cohésion, la 
        paix, le Sahel et la savane.
	</p> <br>
	<h3> Article 4 : Dimension</h3>
	<p>
	   L’APS est une association à dimension continentale et d’ambition fédérative émanant des 
        mouvements des acteurs qui militent en faveur du dialogue entre les peuples, les religions et 
        les cultures en Afrique et dans la diaspora. <br> <br>
        Les activités de l’APS s’exercent sur l’ensemble du territoire burkinabé dans une perspective 
        d’intégration sous régionale et régionale (Bénin, Centrafrique, Cameroun, Gambie, Ghana, 
         Guinée Bissau, Mali, Mauritanie, Niger, Nigeria, Tchad, Sénégal, Soudan, Togo).

	</p> <br>
	<h3> Article 5 : Siège social </h3>
	<p>
       Le siège de l’APS est à Ouagadougou, province du Kadiogo, Région du Centre au Burkina 
       Faso. Ce siège peut être transféré en tout autre lieu au Burkina Faso par décision de 
       l’Assemblée Générale. <br> <br>
        L’APS peut étendre ses représentations dans les pays de la Savane et du Sahel africains.
    </p> <br>
    <h3> Article 6 : Durée de vie </h3>
	<p>
       La durée de vie de l’Association est illimitée .  
    </p> <br>
	<h3> Article 7 : Affiliation </h3>
	<p>
        Dans le respect de sa vision et mission, l’APS peut développer des alliances pour s’affilier à 
        d’autres structures et organisations régionales et internationales poursuivant les mêmes buts 
        et objectifs.
        L’APS peut aussi, avec d’autres associations ou des ONG, entreprendre des actions 
        communes susceptibles de contribuer plus efficacement à la réalisation de ses objectifs.
    </p> <br> <br>

	<h2> TITRE II : BUTS ET OBJECTIFS</h2>
	<h3>Article 8 : Buts </h3>
	<p>
	   L’APS se donne pour but de contribuer à la recherche de la cohésion sociale et de la paix 
       dans les pays du Sahel et de la Savane.
	</p><br>

	<h3>Article 9 : Objectifs </h3>
	<p>
	   L’APS poursuit les objectifs suivants :
		<ul>
			<li>contribuer à la prévention et au règlement des crises et des conflits entre les peuples, les 
            religions et les cultures par le dialogue ; </li>
            <li> soutenir des espaces de dialogue et de réflexion continus sur les options politiques 
            permettant de faire avancer le « mieux vivre-ensemble », la paix et la sécurité dans les 
            pays du Sahel et de la Savane ;</li>
			<li>être une force de propositions qui peut aider à trouver des solutions endogènes pour la 
            prévention des conflits, de l’extrémisme violent et pour aboutir à une gouvernance 
            adaptée aux réalités socio-culturelles des pays du Sahel et de la Savane ; </li>
			<li> jouer un rôle de veille et d’interpellation des intervenants dans la crise sécuritaire dans 
            les pays du Sahel et de la Savane pour la défense et à la promotion des droits humains
            des communautés ;</li>
			<li> contribuer à la lutte contre la désinformation, toutes formes de discriminations et de 
            stigmatisations communautaires des pays du Sahel et de la Savane ;</li>
			<li> promouvoir les innovations agropastorales pour réduire la vulnérabilité des jeunes des 
            zones rurales alimentant les groupes armés semant la terreur au Sahel et en Savane ;</li>
			<li> promouvoir la fraternité et la solidarité entre les membres de l’association ; </li>
			<li> développer des partenariats et des amitiés sur le plan national et international avec des 
            associations qui poursuivent les mêmes objectifs que le APS.</li>
		</ul>
	</p> <br><br>

	<h2>TITRE III : ADHESION-MEMBRES </h2>
	<h3> Article 10 : Adhésion</h3>
	<p>
	   L’adhésion à l’APS est libre et volontaire. L’adhésion ne devient effective qu’après le 
       paiement du droit d’adhésion.
	</p> <br>

	<h3>Article 11 :<em>  Qualité de membre </em> </h3>
	<p>
	   Peut être membre de l’association toute personne physique ou morale qui adhère au but et 
       objectifs fixés aux articles 8 et 9 des présents statuts, sans distinction aucune, et acceptant 
       de se conformer aux textes régissant l’association.
	</p><br>
	<h3> Article 12 :<em> Catégorie de membre </em></h3>
	<p>
	    L’APS comprend les catégories de membres suivants : <br>
		<ul>
			<li> <strong> Sympathisants :</strong> sont des personnes physiques ou morales ressortissantes ou non qui 
            adhèrent aux idéaux de l’association et qui contribuent à des degrés divers à son 
            rayonnement.</li>
			<li> <strong> Fondateurs :</strong> sont les personnes physiques ou morales ressortissantes ou non qui ont 
            contribué concrètement à sa mise en place et qui ont marqué leur engagement en libérant 
            leurs droits d’adhésion, et dont la liste statuts est annexée.</li>
			<li> <strong> Membres actifs :</strong> sont ceux qui s’acquittent des droits d’adhésion, s’acquittent
            régulièrement de leurs cotisations et participent de manière effective à la vie et aux 
            activités de l’association. Ils ont voix délibérative. </li>
			<li> <strong> Membres d’honneurs : </strong>Les adhérents méritants et désignés par l’APS pour honorer de 
            leurs présences ou de leurs soutiens multiformes. </li>
			<li> <strong> Membres donateurs :</strong> Les adhérents reconnus par les différentes instances et organes 
            de l’APS pour leurs accompagnements et leurs soutiens divers dans l’atteinte et la 
            réalisation des activités et actions prévues. </li>
		</ul>

	</p> <br>
	<h3> Article 13 :<em> Perte de la qualité de membre </em>  </h3>
	<p>
	    La perte de la qualité de membre intervient dans les cas ci-après : <br>
		<ul>
		 <li> le non-paiement de ses cotisations pendant 2 ans ; </li>
		 <li>la démission et l’exclusion suite à une faute grave ;</li>
		 <li> le décès ; </li>
		 <li> la dissolution de l’association.</li>
		</ul> <br>
		Toutefois, l’exclusion est appréciée et prononcée par le Bureau Exécutif (BE) après avoir 
        préalablement appelé le membre intéressé à fournir ses explications par écrit ou par message 
        vocal. Cette décision du BE est soumise à l’appréciation de l’Assemblée Générale.


	</p> <br> <br>
	<h2> TITRE IV : DROITS ET OBLIGATIONS DES MEMBRES </h2>
	<h3>  Chapitre I: Droits et obligations des membres actifs</h3>
	<h3> Article 14 : Droits </h3>
	<p>
	   Tous les membres actifs de l’association jouissent des mêmes droits et disposent chacun 
       d’une seule voix en cas de vote à l’AG. <br>
	   A ce titre, ils ont droit : <br>
	   - d’être informés sur la vie et les activités de l’association ; <br>
       - de participer aux activités initiées par l’association ; <br>
       - de faire des suggestions de nature à améliorer le fonctionnement et les activités de 
        l’association ; <br>
        - d’exprimer librement leur opinion sur toutes les questions relatives à la vie de 
        l’association dans le cadre statutaire sans toutefois porter atteinte à l’image de la 
        structure ; <br>
       - d’être électeur et éligible aux instances et organes de l’association dans les conditions 
       définies par les statuts.


	</p> <br>
	<h3> Article 15 : Obligations</h3>
	<p>
	  Tous les membres actifs de l’association sont soumis aux mêmes obligations. <br>
      A ce titre, ils sont tenus de : <br>
       - payer leurs frais d’adhésion et leurs cotisations ; <br>
       - respecter la discipline intérieure de l’association telle qu’elle est précisée dans les 
       statuts et le règlement intérieur ; <br>
       - participer activement à la vie et aux activités de l’association;
       - faire la promotion de l’association et ses actions ; <br>
       - participer aux différentes réunions des organes et aux activités ; <br>
       - s’acquitter de tous autres devoirs en conformité avec les objectifs de l’association ; <br>
       - travailler à promouvoir l’image de l’association ; <br>
       - se soumettre aux décisions de l’AG ou du BE. 
	</p> <br> <br>
	<h2> Chapitre II: Droits et Obligations des membres sympathisants, d’honneur et donateurs </h2>
	<h3> Article 16 : Droits</h3>
	<p>
	   Les membres sympathisants, d’honneur et donateurs ne jouissent pas des mêmes droits que 
       les membres statutaires et actifs de l’association car : <br>
      - ils ne sont ni électeurs, ni éligibles ; <br>
       - ils ne participent pas aux prises de décisions de l’association ;<br>
        - ils ne paient ni frais d’adhésion et ni de cotisations. <br>
       Toutefois, ils peuvent : <br>
        - prendre part aux activités de l’association dans les limites des dispositions 
        statutaires ; <br>
      - faire toute suggestion de nature à améliorer le fonctionnement de l’association et la 
      mise en œuvre de ses activités.


	</p> <br>
	<h2> Article 17 : Obligations </h2>
	<p>
       Ils sont tenus de : <br>
       - respecter la discipline intérieure de l’association telle qu’elle est précisée dans les 
        statuts et le règlement intérieur ; <br>
       - défendre les intérêts matériels et moraux de l’association ; <br>
       - se soumettre aux décisions de l’association.
    </p> <br> <br>
	<h2> TITRE V : STRUCTURES ET FONCTIONNEMENT </h2>
	<h3> Chapitre I : Structures </h3>
	<p>
		<strong> Article 18 : </strong> Les instances et organes du APS sont : <br>
		- l’Assemblée Générale ; <br>
        - le Bureau Exécutif ;<br>
        - les Antennes Nationales ;<br>
        - les Coordinations Régionales ;<br>
        - les Commissions Spécialisées ;<br>
        - les Commissaires aux Comptes ;


	</p> <br>
	<h3>Chapitre II : Fonctionnement</h3> <br>
	<h3>Section 1 : Assemblée Générale</h3> <br>
	<h3>Article 19 : Attributions de l’AG </h3> <br>
	<p>
	    L’Assemblée Générale est l’instance suprême d’administration de l’association. Elle se 
        réunit en session ordinaire une (01) fois par an à son siège. La convocation de l’AG est à 
        l’initiative du président. Ses délibérations sont adoptées au 2/3 au moins des membres 
        présents. <br>
        Chaque membre est tenu d’assister personnellement aux AG. En cas d’empêchement, il 
        pourra communiquer son avis sur l’ordre du jour à tout membre de son choix auquel il a 
        donné mandat écrit et qui lui rendra compte des délibérations de l’AG.
       <br>
       Les décisions de l’AG sont exécutoires et s’appliquent à tous les membres de l’association. <br>
       L’AG se réunit en session ordinaire conformément aux dispositions statutaires et en session 
       extraordinaire chaque fois que de besoin. 



	</p> <br>
	<h3>Article 20 : Composition de l’AG </h3>
	<p>
        L’Assemblée Générale regroupe : <br>
        - les membres du BE ;<br>
        - les délégués mandatés par les Antennes Nationales ;<br>
        - les délégués mandatés par les Coordinations Régionales ;<br>
        - les membres d’honneurs ;<br>
        - les Commissaires aux Comptes ;<br>
        - les représentants/tes des partenaires techniques et financiers de l’Association 
        participent à l’A.G. <br> 
        Les délégués à l’Assemblée Générale sont désignés par les Antennes Nationales et les 
        Coordinations Régionales au cours d’une rencontre organisée à cet effet. Le nombre de 
        délégués par Antennes Nationales et les Coordinations Régionales est fixé à deux (2) 
        personnes.
    </p> <br> 
	<h3> Article 21 : Attributions de l’Assemblée Générale Ordinaire (AGO)</h3> 
	<p>
        L’Assemblée Générale Ordinaire décide d’une manière générale de la gestion et de 
        l’administration de l’association. L’AGO est présidée par le président du BE et est 
        compétente pour : <br>
        - déterminer la politique générale de l’association ;<br>
        - élire ou révoquer les membres du bureau ;<br>
        - approuver ou désapprouver le bilan moral et financier ;<br>
        - fixer le montant et les modalités de recouvrement des droits d’adhésion, des 
        cotisations annuelles et spécifiques s’il y a lieu.
    </p> <br>
	<h3> Article 22 : Convocation de l’AGO </h3> 
	<p>
      L’AGO est convoquée quinze (15) jours avant la date de sa tenue, par le président du BE qui
      informe par tout moyen les membres de la tenue de l’AG, de la date, du lieu, de l’heure et 
      de l’ordre du jour.
    </p> <br>
	<h3>Article 23 : Délibération de l’AGO </h3> 
	<p>
        L’AGO délibère valablement à la majorité simple des membres présents et votants.
    </p> <br>
	<h3> Article 24 : Attributions de l’Assemblée Générale Extraordinaire (AGE) </h3> <br>
	<p>
	
      L’AGE est présidée par le président du BE et est compétente pour : <br>
      - statuer sur les problèmes urgents pour lesquels elle a été convoquée ; <br> 
      - modifier les statuts et/ou le règlement intérieur de l’association ; <br>
      - décider de la fusion ou de la dissolution de l’association.
   </p> <br>
  <h3> Article 25 : Convocation de l’AGE </h3> 
  <p>
      L’AGE est convoquée par le président ou à la demande des deux tiers (2/3) des membres du 
      BE pour discuter de problèmes urgents qui concernent l’association. Elle est convoquée au 
      moins sept (07) jours avant la date de sa tenue, par le président du BE qui informe par tout 
      moyen les membres de la tenue de l’AGE, de la date, du lieu, de l’heure et de l’ordre du jour.
    </p> <br>
	<h3></h3> Article 26 : Délibération de l’AGE </h3> 
	<p>
      L’AGE ne peut délibérer valablement qu’au deux tiers (2/3) des membres présents et votants.
    </p> <br>
<h3>	Section 2 : Bureau Exécutif (BE) </h3> 
<h3>  Article 27: Attributions du BE </h3> 
<p>
   Le BE est l’organe exécutif de l’association. Il assure l’administration de l’association 
   pendant la durée de son mandat.
</p> <br>
<h3>Article 28: Composition du BE </h3> 
<p>
   Le Bureau Exécutif du APS comprend dix (10) membres : <br>
   - Un/une Président,  <br>
   - Un/une Vice-Président,<br>
   - Un/une Secrétaire Général,<br>
   - Un/une Secrétaire à la mobilisation des ressources financières, <br>
   - Un/une Secrétaire à l’information et à la communication,<br>
   - Un/une Secrétaire chargé des projets et programmes,<br>
   - Un/une Secrétaire à la coopération et au partenariat,<br>
   - Un/une Secrétaire à l’organisation et aux activités socioculturelles,<br> 
   - Un/une Secrétaire à la promotion de l’équité, de genre et de l’inclusion sociale,<br> 
   - Un/une Secrétaire à la coordination des démembrements.
</p> <br>
<h3> Article 29 : Attributions des membres du BE </h3> 
<p>
   Les attributions des membres du BE sont définies dans le règlement intérieur de 
   l’association. 
</p> <br>
<h3>Article 30: Convocation du BE </h3> 
   Le BE se réunit sur convocation du président chaque fois que de besoin, et dans tous les cas, 
   une fois par trimestre.
</p><br>
<h3>Article 31: Délibérations du BE </h3> 
   Les délibérations du BE font l’objet d’un compte rendu et sont portées à la connaissance de 
   tous membres de l’association lors des AG suivantes.
</p> <br>
<h3>Article 32 : Mandat du BE </h3> 
<p>
   Le BE est élu en AG pour trois (03) ans. Est éligible tout membre actif et à jour de ses 
   cotisations. Les membres du BE sont rééligibles. La révocation collective ou individuelle 
   intervient dans les mêmes conditions que l’élection.
</p> <br>
 <h3>Section 3 : Antennes Nationales (AN) </h3> 
 <h3>Article 33 : Mise en place des AN </h3> 
 <p>
     Les membres de l’APS autres que ceux du Burkina Faso peuvent s’organiser au sein de 
     chaque Etat en Antenne Nationale qui est un organe du APS. <br>
     Les Antennes Nationales de l’APS sont des plateformes d’échanges et réflexions dans les 
     différents pays membres. Elles s’organisent conformément au droit national des Etats. <br> 
     Chaque AN doit disposer d’une reconnaissance officielle dans le pays d’exercice lui 
     conférant une autonomie morale et institutionnelle. L’AN peut s’affilier, au niveau national, 
     avec d’autres cadres de concertation pour des objectifs communs à atteindre. <br>
     Les Antennes Nationales doivent obtenir une autorisation préalable du BE du APS avant de 
     s’affilier à d’autres plateformes thématiques sous régionales. L’Antenne Nationale et les 
     organisations sœurs membres de la même coalition doivent agir en synergie de sortent à 
     éviter d’être concurrentes. <br> 
     Les statuts et règlement intérieur de l’Antenne Nationale ne peuvent déroger aux statuts et 
     règlement intérieur de l’APS, notamment en ce qui concerne le but, les objectifs et la 
     composition du bureau exécutif . <br>
</p> <br>
<h3>Article 34 : Attributions des Antennes Nationales</h3> 
<p>
  Les Antennes Nationales sont des plateformes nationales représentatives, dynamiques et 
  capables de mobiliser toutes les composantes du mouvement associatif en faveur du dialogue 
  entre les communautés, les cultures et les religions pour influencer les politiques publiques
  dans chaque pays du Sahel et de la Savane. <br>
  Elles structurent leur programme de travail autour du plan stratégique de l’APS, tout en 
  tenant compte des spécificités du pays et de l’expérience des organisations de promotion de 
  la cohésion sociale et de la paix.  <br>
  Elles travaillent de manière transparente avec les organisations sœurs membres et peuvent 
  avoir recours à leurs ressources humaines et à leur expertise, en accord avec les organisations 
  concernées.

</p> <br>
<h3> Section 4 : Coordinations Régionales </h3> 
<h3>Article 35 : Mise en place des Coordinations Régionales (CR) </h3> 
<p>
  Les Coordinations Régionales (CR) sont les organes techniques du APS au niveau de chaque 
  région administrative. <br>
  Elles sont représentées par des bureaux régionaux composés de quatre (04) membres qui 
  sont chargés de coordonner avec les BE, les activités au niveau de chaque région 
  administrative au Burkina Faso et les bureaux des Antennes Nationales pour les autres pays. <br> 
  Les coordinations régionales en tant que démembrements techniques du APS sont soumises 
  aux textes fondamentaux de celui-ci.
</p> <br>

<h3> Article 36 : Composition des CR </h3> 
<p>
  Les bureaux des CR se composent :<br>
  - un Secrétaire Général ; <br>
  - un Trésorier ;<br>
  - un Secrétaire à l’Information et à la Solidarité ;<br>
  - un Secrétaire à l’Organisation et à la Formation.
</p> <br>
<h3> Article 37 : Attributions des CR </h3> 
<p>
   Le bureau du CR est chargé de représenter le BE de l’APS ou le bureau de l’Antenne 
   Nationale dans la région administrative. Il doit : <br>
   - animer la vie de l’association dans la région ; <br>
   - susciter l’intéressement et l’adhésion des membres aux idéaux du APS ; <br>
   - faire prendre en compte les préoccupations du APS dans la région au cours des débats 
   ou des rencontres ; <br>
   - sensibiliser et former les membres sur la cohésion sociale, la paix, les droits humains 
   et la citoyenneté et toutes les questions traitées par l’AG dans les régions concernées ; <br>
   - diffuser et faire appliquer les mesures et recommandations prises par le BE de l’APS.
</p> <br>
<h3> Section 4 : Commissions Spécialisées (CS) </h3> 
<h3>Article 38 : Mise en place des CS </h3> 
<p>
   Le BE ainsi que les Antennes Nationales peuvent mettre en place des commissions 
   spécialisées pour faire face à l’organisation d’activités d’envergure ou pour traiter des 
   questions spécifiques (nexus humanitaire- développement-paix, protection des droits 
   humains, cohésion sociale, dialogue des cultures et des religions, innovations agropastorales, 
   accès aux ressources naturelles, transhumance transfrontalière et foncier pastoral; éducation 
   et alphabétisation, etc.). <br>
   Les commissions spécialisées ou commissions ad hoc ne sont pas permanentes.  <br>
   Les modalités de fonctionnement des commissions spécialisées sont fixées par le BE ou les 
   Antennes Nationales. Elles peuvent être modifiées ou dissoutes selon les circonstances.  <br>
   Leurs activités peuvent nécessiter la désignation d’une expertise ou de personnes ressources 
   qui va donner lieu à une consultation interne.  <br>
   Cette consultation doit être approuvée à la majorité totale des membres du Bureau Exécutif 
   pour la signature d’une convention de recrutement. En cas de non-consensus sur la 
   désignation, l’appel à candidature pour le recrutement d’un consultant individuel ou d’un 
   cabinet reste la procédure établie.

</p> <br>
<h3> Section 5 : Commissaires aux Comptes (CC) </h3> 
<h3>Article 39 : Mise en place des CC </h3> 
<p>
Les ressources de l’association sont contrôlées par deux (02) commissaires aux comptes,
élus par l’AG pour un mandat de trois (03) ans renouvelable une fois. <br>
Ils exercent leurs contrôles conformément aux dispositions contenues dans le Règlement 
intérieur.
</p> <br>
<h3> Article 40 : Attributions des CC </h3> 
<p>
Les Commissaires aux comptes sont chargées d'émettre des avis motivés sur le 
fonctionnement administratif et financier du APS, de contrôler la mise en œuvre effective
des orientations stratégiques de l’AG et de présenter, dans ces domaines, des propositions 
d'orientation ou de réajustement. <br>
De façon spécifique, les Commissaires aux comptes sont chargés :<br>
- du contrôle interne ;
- de veiller au respect des statuts et du règlement intérieur,<br>
- d’effectuer des contrôles inopinés des ressources de l’APS ;<br>
- de présenter un rapport de contrôle interne à l’AG.


</p> <br> <br>
<h2> TITRE VI : RESSOURCES ET LEUR GESTION</h2> 
<h3> Article 41 : Ressources </h3> 
<p>
Les ressources de l’APS sont essentiellement constituées par : <br>
- les droits d’adhésion <br>
- les cotisations de ses membres <br>
- les quêtes <br>
- les dons et legs <br>
- les subventions <br>
- les cotisations exceptionnelles dont le montant est fixé par la réunion des 
membres <br>
- les rétributions perçues pour service rendu <br>
- les produits provenant de la location des biens meubles et immeubles de 
l’association.
</p> <br>
<h3>  Article 42 : Utilisation des ressources et des biens</h3> 
<p>
Les ressources financières et les biens de l’APS sont exclusivement utilisés au seul bénéfice 
de l’association pour l’exécution de sa mission. Les fonds doivent être déposés dans une 
institution financière de la place au nom de l’association. Les signataires pour des comptes 
sont le Président et le Secrétaire à la mobilisation des ressources financières. En cas 
d’absence de l’un d’eux, le Vice-président ou le Secrétaire général le remplace.

</p> <br>
<h3>  Article 43 : Dépenses</h3> 
<p>
Le président de l’Association est ordonnateur du budget de l’association. Les dépenses sont 
faites conformément aux prévisions budgétaires. <br>
Les fonctions de membres de l’APS ne donnent pas lieu à une rémunération. Toutefois, 
certaines dépenses peuvent donner lieu à des remboursements de frais autorisés par le bureau 
sur présentation de justificatifs. <br>
Les diverses fonctions de responsabilité sont exercées dans le strict respect des statuts et 
règlement intérieur. 
</p> <br>
<h3> Article 44 : Comptabilité </h3> 
<p>
Il est tenu une comptabilité régulière conformément aux normes comptables en vigueur au 
Burkina Faso, dont le Secrétaire à la mobilisation des ressources financières rend compte 
annuellement à travers : <br>
- un rapport d’activité ; <br>
- un rapport financier
</p> <br>
<h3>Article 45 : Contrôle </h3> 
<p>
Les comptes de l’APS et les rapports annuels font l’objet d’un bilan adressé à l’AG, instance 
suprême de contrôle du BE. Les partenaires qui vont apporter leurs contributions à l’APS
peuvent toujours réaliser un audit financier pour s’assurer de la bonne gestion des fonds 
alloués.
</p> <br> <br>
<h2> TITRE VII : DISPOSITIONS DIVERSES </h2> 
<h3> Article 46: </h3>
<p>
Tout membre de l’association est tenu au respect des présents statuts dont la 
violation constitue une faute pouvant faire l’objet de sanctions.
</p> <br>
<h3> Article 47 : Modifications des statuts </h3> 
<p>
Les présents statuts ne peuvent être révisés, amendés, modifiés que par la réunion de l’AGE, 
sur proposition du Bureau Exécutif et /ou des 2/3 des membres. Les décisions de
l’Assemblée Générale Ordinaire sont prises à la majorité simple des membres présents, après 
recherche d’une décision consensuelle. <br>
Les décisions de l’Assemblée Générale Extraordinaire relatives à la modification des textes 
de l’association, et à la dissolution sont prises à la majorité des deux tiers (2/3) des membres 
présents et votants.
</p> <br>
<h3> Article 48 : Dissolution</h3> 
<p>
La dissolution de l’association ne peut être prononcée qu’en Assemblée Générale 
Extraordinaire convoqué à cet effet. Les décisions de l’Assemblée Générale Extraordinaire 
relatives à la dissolution sont prises à la majorité des deux tiers (2/3) des membres présents 
et votants. <br>
En cas de dissolution, les biens de l’Association seront transférés à un organisme poursuivant 
le même but.
</p> <br>
<h2> TITRE VIII. DISPOSITIONS FINALES</h2> 
<h3> Article 49 : Règlement des litiges</h3> 
<p>
En cas de conflit ou litige dans l’interprétation ou dans la mise en œuvre des présents statuts, 
Il sera privilégié le règlement à l’amiable. En cas d’échec, la juridiction du pays siège de 
l’APS est compétente.	
</p> <br>
<h3> Article 50 : </h3> 
<p>
un règlement intérieur précisera les modalités d’application des présents statuts. 
Il en constitue une partie intégrante après leur adoption par l’Assemblée Générale.

</p> <br>
